public class dateChangers {

	@Keyword
	def changeDate (String dateTarget, int nextDay) {

		String dt = dateTarget;  // Start date
		SimpleDateFormat sdf = new SimpleDateFormat("MMMM d, yyyy");
		Calendar c = Calendar.getInstance();
		c.setTime(sdf.parse(dt));
		c.add(Calendar.DATE, nextDay);  // number of days to add
		dt = sdf.format(c.getTime());  // dt is now the new date

		return dt

	}
}
